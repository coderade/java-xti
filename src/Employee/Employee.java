package Employee;

public class Employee 
{
	private String name;
	public int age;
	private Boolean status;
	
	
	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}
	
	public Boolean isStatus() 
	{
		return status;
	}

	public void setStatus (boolean status)
	{
		this.status = status;
	}
	}

